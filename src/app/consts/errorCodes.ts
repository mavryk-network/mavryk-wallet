export const getErrorMsgByCode = (code: string | number) => {
  try {
    const errCode = typeof code === 'number' ? code.toString() : code;
    // eslint-disable-next-line no-type-assertion/no-type-assertion
    const errMsg = rwaMarketplaceErrorCodes[errCode as keyof typeof rwaMarketplaceErrorCodes];

    return errMsg ?? 'Something went wrong!';
  } catch (e) {
    return 'Something went wrong!';
  }
};
